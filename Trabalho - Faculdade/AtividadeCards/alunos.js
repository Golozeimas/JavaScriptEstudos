document.getElementById("btn").addEventListener('click', ()=>{
    let container = document.getElementById("lista")

        container.innerHTML = ""
        
        data.forEach(aluno => {
        // Cria o Card
        let card = document.createElement('div')
        card.classList.add("card")

        // Cria o nome
        let nome = document.createElement('h3')
        nome.textContent = aluno.nome

        // Cria o cargo
        let cargo = document.createElement('p')
        cargo.textContent = aluno.cargo 

        // Cria o rodape
        let rodape = document.createElement('div')
        rodape.classList.add('rodape')

        // Cria a stack
        let stack = document.createElement('span')
        stack.textContent = aluno.stack

        // Cria o nivel
        let nivel = document.createElement('span')
        nivel.textContent = aluno.nivel
        nivel.classList.add('badge')

        // Montando o rodape
        rodape.appendChild(stack)
        rodape.appendChild(nivel)

        // Montando o Card
        card.appendChild(nome)
        card.appendChild(cargo)
        card.appendChild(rodape)

        // Adicionando o card na lista
        container.appendChild(card)
    })
})


